<h2>Welcome to The Hive</h2>

<p>The Hive is an online community, where bees from many fields come to chat, meet new friends and play games. Whether you are new or established, you can be sure of a warm welcome.</p>

<p>The majority of the users are UK based, but we have a growing number of users from all over the world, meaning that even in the small hours The Hive is often still busy.</p>

<p>You may ask yourself, "Why should I log on here? What does The Hive offer that other places don't?"</p>

<ol id="whyhive">
	<li>
		<h4>Equality</h4>
		<p>No user has more authority than any other. You don't need to be granted any special privileges to create socials, play games, or to create and set up your own chat groups.</p>
	</li>
	<li>
		<h4>Freedom</h4>
		<p>There is no restriction on what you can say or do on The Hive. This may sound uncontrolled, but thanks to the sophisticated <a href="?features" title="Talker Features">ignore system</a>, a friendly, sociable and orderly environment is actually the result.</p>
	</li>
	<li>
		<h4>Originality</h4>
		<p>Rather than copy the functionality of other talkers and chat sites, we've introduced a different look and feel along with different ways of doing things, which we feel are more intuitive and make the place more accessible to new and old users alike.</p>
	</li>
	<li>
		<h4>Staffless</h4>
		<p>Unlike other chat rooms, The Hive is completely staff free. The result of this being that each person connecting is equal, which means many of the disagreements and petty power struggles on other places just don't happen! Read our <a href="?stafflessfaq" title="Staffless Talker - Frequently Asked Questions">Staffless FAQ</a> to find out just how this system of equality actually works.</p>
	</li>
	<li>
		<h4>Fun</h4>
		<p>Most importantly The Hive is fun. No one takes themselves too seriously. There are games to play, quizes to take part in, and friendly users to talk to, all waiting for you right now.</p>
	</li>
</ol>

<p>With all this in mind, why not <a href="?connecting" title="Connecting to The Hive">connect</a> and see for yourself...?</p>

