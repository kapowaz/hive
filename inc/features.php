<h2>Features</h2>

<p>The Hive is unique within the Talker community for a multitude of reasons. Here are a few of the key features of the talker that set it apart from the rest of the crowd.</p>

<ul>
	<li>
		<h4>No staff</h4>
		<p>The Hive is founded on the principal idea of equality for all; that is to say there is no privilege system, no commands which can be used on another user which they cannot use as well, and no elitist culture. The staffless argument is complex, and many frequently asked questions are <a href="?stafflessfaq" title="Staffless Talker - Frequently Asked Questions">answered here</a>.</p>
	</li>
	<li>
		<h4>Sophisticated ignore system</h4>
		<p>In order for a staffless system to work, a decent ignore system was required. This is still evolving and being improved to this day, but already has a massive amount of flexibility in terms of what you can choose to see and not see.</p>
		<p>Currently anyone can choose to ignore any of the following:</p>
		<ul>
			<li><em>Public</em> - communication on public and invite-only groups</li>
			<li><em>Private</em> - communication to you that no-one else will see</li>
			<li><em>Directed</em> - directed communication, such as socials, on groups</li>
			<li><em>Games</em> - requests to play games from user(s)</li>
			<li><em>Socials</em> - socials performed by user(s)</li>
			<li><em>Socials By</em> - socials created by user(s)</li>
			<li><em>Connections</em> - messages regarding connections, disconnections and reconnections</li>
			<li><em>Set Text</em> - text set by users for things such as topic comments and titles</li>
			<li><em>Announcements</em> - announcement messages associated with user(s)</li>
		</ul>
		<p>Any of these options can be applied to individual users, or groups of users - including new bees or indeed everyone.</p>
	</li>
	<li>
		<h4>Groups system</h4>
		<p>The Hive features a flexible groups system to allow users to discuss specific subjects in a self-contained environment.</p>
		<p>Hive Groups differ from channels or rooms, if you are familiar with these features on other talkers, in that they can be created by anyone, can be public or private, and users are free to define their own colour scheme, commands and interface to the talker.</p>
		<p>A user can even set a specific group to be their default so the basic commands (say, emote, think, sing and so forth) go to this group as standard.</p>
	</li>
</ul>